package gen

import (
	"bytes"
	"fmt"
	"strings"
	"unicode"

	engine "github.com/taubyte/tau/pkg/tcc/engine"
)

// TypeScript emission. Reuses the same DSL walk as the Go struct proposals
// (Structs) — resource/field selection is language-neutral — but emits idiomatic
// TypeScript rather than a 1:1 Go mirror: camelCase properties, optional fields
// for everything the DSL does not mark Required(), and string-literal union types
// for InSet-constrained fields.

// tsType maps a generated Go field type to its TypeScript equivalent.
func tsType(goType string) string {
	switch goType {
	case "string":
		return "string"
	case "[]string":
		return "string[]"
	case "bool":
		return "boolean"
	case "int", "uint64":
		return "number"
	default:
		return "unknown"
	}
}

// tsName lower-camelCases a Go field name, collapsing leading acronym runs
// (FQDN -> fqdn, MQTT -> mqtt, WebSocket -> webSocket, CertType -> certType).
func tsName(goField string) string {
	r := []rune(goField)
	for i := 0; i < len(r); i++ {
		if !unicode.IsUpper(r[i]) {
			break
		}
		if i > 0 && i+1 < len(r) && unicode.IsLower(r[i+1]) {
			break // r[i] begins a new word
		}
		r[i] = unicode.ToLower(r[i])
	}
	return string(r)
}

// tsFieldType is the property's TypeScript type: a union of literals for an
// InSet field, otherwise the mapped scalar type.
func tsFieldType(f Field) string {
	if len(f.Enum) == 0 {
		return tsType(f.Type)
	}
	parts := make([]string, len(f.Enum))
	for i, v := range f.Enum {
		if f.EnumString {
			parts[i] = `"` + v + `"`
		} else {
			parts[i] = v
		}
	}
	return strings.Join(parts, " | ")
}

// GenerateTS renders every DSL resource as a TypeScript interface into a single
// schema.ts.
func GenerateTS(root []*engine.Node) ([]byte, error) {
	models, err := Structs(root)
	if err != nil {
		return nil, err
	}

	var b bytes.Buffer
	b.WriteString("// Code generated by tcc-gen; DO NOT EDIT.\n")
	b.WriteString("// Resource schemas derived from the tcc DSL (pkg/tcc/.../schema).\n\n")

	for _, m := range models {
		fmt.Fprintf(&b, "export interface %s {\n", m.Spec)
		for _, f := range m.Fields {
			opt := "?"
			if f.Required {
				opt = ""
			}
			fmt.Fprintf(&b, "  %s%s: %s;\n", tsName(f.Name), opt, tsFieldType(f))
		}
		b.WriteString("}\n\n")
	}

	return b.Bytes(), nil
}
